package com.leaselens.model;

/**
 * This enum is for the different statuses an apartment can have
 * User can mark apartment as new, shortlisted, toured, or rejected
 *
 * pre-condition: none
 * post-condition: one of four status values is used
 */
public enum Status {
    NEW,
    SHORTLISTED,
    TOURED,
    REJECTED
}
